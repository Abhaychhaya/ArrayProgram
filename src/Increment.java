
public class Increment {

	public static void main(String[] args) {
		
		int number  = 50;
		
		System.out.println("Number is " + number);
		
		//Increment number 
		
		number++;
		
		System.out.println("Number is " + number);
		
		//Decrement number 
		
		number--;
		
		System.out.println("Number is " + number);
		
		

	}

}
